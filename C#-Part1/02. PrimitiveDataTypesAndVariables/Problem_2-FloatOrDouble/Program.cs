using System;

class FloatOrDouble
{
    static void Main()
    {
        double num1 = 34.567839023;
        float num2 = 12.345F;
        double num3 = 8923.1234857;
        float num4 = 3456.091F;
        Console.WriteLine ("The numbers are {0}, {1}, {2}, and {3}.", num1, num2, num3, num4);
    }
}
